<script>

  // Smooth scroll untuk navigasi
  document.querySelectorAll('.nav a').forEach(anchor => {
      anchor.addEventListener('click', function(e) {
          e.preventDefault();
          document.querySelector(this.getAttribute('href')).scrollIntoView({
              behavior: 'smooth'
          });
      });
  });

  // Banner text fade-in animation
  window.addEventListener('load', function() {
      const bannerText = document.querySelector('.banner h1');
      bannerText.style.opacity = 0;
      bannerText.style.transform = 'translateY(20px)';
      setTimeout(() => {
          bannerText.style.transition = 'opacity 1s ease-out, transform 1s ease-out';
          bannerText.style.opacity = 1;
          bannerText.style.transform = 'translateY(0)';
      }, 300);
  });

  // Animasi pada label "Sisa kamar"
  document.querySelectorAll('.availability').forEach(label => {
      setInterval(() => {
          label.style.transition = 'opacity 0.8s ease';
          label.style.opacity = label.style.opacity == 1 ? 0.6 : 1;
      }, 1000);
  });

  
</script>